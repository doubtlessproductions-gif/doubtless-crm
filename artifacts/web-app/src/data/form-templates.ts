// 100+ prebuilt form templates for the Form Builder

export interface TplField {
  type: string;
  label: string;
  required?: boolean;
  options?: string[];
  content?: string;
  helpText?: string;
  placeholder?: string;
  crmField?: string;
  min?: number;
  max?: number;
  step?: number;
  unit?: string;
  maxStars?: number;
  matrixRows?: string[];
  matrixCols?: string[];
  yesLabel?: string;
  noLabel?: string;
}

export interface FormTemplate {
  id: string;
  name: string;
  category: string;
  description: string;
  tags?: string[];
  fields: TplField[];
}

const h = (text: string): TplField => ({ type: "heading", label: text, content: text, required: false });
const div = (): TplField => ({ type: "divider", label: "", required: false });
const txt = (label: string, opts?: Partial<TplField>): TplField => ({ type: "short_text", label, required: false, ...opts });
const ltxt = (label: string, opts?: Partial<TplField>): TplField => ({ type: "long_text", label, required: false, ...opts });
const em = (label = "Email", opts?: Partial<TplField>): TplField => ({ type: "email", label, crmField: "email", required: true, ...opts });
const ph = (label = "Phone", opts?: Partial<TplField>): TplField => ({ type: "phone", label, crmField: "phone", required: false, ...opts });
const nm = (label = "Full Name", opts?: Partial<TplField>): TplField => ({ type: "full_name", label, crmField: "name", required: true, ...opts });
const dt = (label: string, opts?: Partial<TplField>): TplField => ({ type: "date", label, required: false, ...opts });
const dd = (label: string, options: string[], opts?: Partial<TplField>): TplField => ({ type: "dropdown", label, options, required: false, ...opts });
const rb = (label: string, options: string[], opts?: Partial<TplField>): TplField => ({ type: "radio", label, options, required: false, ...opts });
const cb = (label: string, opts?: Partial<TplField>): TplField => ({ type: "checkbox", label, required: false, ...opts });
const cbg = (label: string, options: string[], opts?: Partial<TplField>): TplField => ({ type: "checkbox_group", label, options, required: false, ...opts });
const num = (label: string, opts?: Partial<TplField>): TplField => ({ type: "number", label, required: false, ...opts });
const sig = (label = "Signature", opts?: Partial<TplField>): TplField => ({ type: "signature", label, required: true, ...opts });
const init = (label = "Initials", opts?: Partial<TplField>): TplField => ({ type: "initials", label, required: true, ...opts });
const dsig = (label = "Date Signed"): TplField => ({ type: "date_signed", label, required: true });
const ctxt = (label: string, content: string): TplField => ({ type: "contract_text", label, content, required: false });
const legal = (label: string, content: string): TplField => ({ type: "legal_agreement", label, content, required: true });
const stmt = (label: string, content: string): TplField => ({ type: "statement", label, content, required: false });
const instr = (label: string, content: string): TplField => ({ type: "instructions", label, content, required: false });
const rating = (label: string, opts?: Partial<TplField>): TplField => ({ type: "rating", label, maxStars: 5, required: false, ...opts });
const scale = (label: string, opts?: Partial<TplField>): TplField => ({ type: "scale", label, min: 1, max: 10, required: false, ...opts });
const yn = (label: string, opts?: Partial<TplField>): TplField => ({ type: "yes_no", label, required: false, ...opts });
const url = (label: string, opts?: Partial<TplField>): TplField => ({ type: "url", label, required: false, ...opts });
const addr = (label = "Address", opts?: Partial<TplField>): TplField => ({ type: "address", label, required: false, ...opts });
const curr = (label: string, opts?: Partial<TplField>): TplField => ({ type: "currency", label, required: false, ...opts });
const pct = (label: string, opts?: Partial<TplField>): TplField => ({ type: "percentage", label, required: false, ...opts });
const spacer = (): TplField => ({ type: "spacer", label: "", required: false });

